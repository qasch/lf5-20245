# Strings in Python

my_string = "Ich bin ein String"

print(my_string)

my_second_string = " und ich noch einer"
print(my_second_string)

# Strings verbinden
big_string = my_string + my_second_string
print(big_string)

for number in range(10):
    print(number)

for character in my_string:
    print(character)
